# Instalar la extensión de Python

# Para crear un virtual enviroment en VS Code tenemos que abrir la terminal y escribir el siguiente comando:
#   python3 -m venv nombre
# Se creará una carpeta llamada "nombre" con Python 3 como lenguaje.

# O hacer: Ctrl + Shift + P, escribir Python y buscar la opción de Crear Virtual Enviroment y seleccionar un interperetador de Python.

# Comandos:

# Cambiar de directorio: 
#   cd "nombre de directorio"

# Ir a la carpeta anterior: 
#   cd ..

# Ejecutar streamlit: 
#   streamlit run "nombre_archivo"

# Detener ejecución: 
#   Ctrl + C

# Para crear requirements.txt usamos la librería freeze
#   pip install freeze-requirements
#   pip freeze > requirements.txt
 
# Para actualizar una librería a su última versión:
#   pip install -U "librería"
